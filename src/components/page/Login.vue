<template>
    <div class="login-wrap">
        <div class="ms-login">
            <div class="ms-title">后台管理系统</div>
            <el-form :model="ruleForm" :rules="rules" ref="ruleForm" label-width="0px" class="ms-content">
                <el-form-item prop="username">
                    <el-input v-model="ruleForm.username" placeholder="username">
                        <el-button slot="prepend" icon="el-icon-lx-people"></el-button>
                    </el-input>
                </el-form-item>
                <el-form-item prop="password">
                    <el-input type="password" placeholder="password" v-model="ruleForm.password" @keyup.enter.native="isLogin(ruleForm)">
                        <el-button slot="prepend" icon="el-icon-lx-lock"></el-button>
                    </el-input>
                </el-form-item>
                <div class="login-btn">
                    <el-button type="primary" @click="isLogin(ruleForm)">登录</el-button>
                </div>
            </el-form>
        </div>
    </div>
</template>

<script>
    export default {
        data: function(){
            return {
                url: '../static/vueLogin.json',
                ruleForm: {
                    username: '',
                    password: '',
                },
                rules: {
                    username: [
                        { required: true, message: '请输入用户名', trigger: 'blur' }
                    ],
                    password: [
                        { required: true, message: '请输入密码', trigger: 'blur' }
                    ]
                }
            }
        },
        methods: {
            isLogin(formName) {
                this.$axios.get(this.url).then((res) => {
                    console.log(res.data);
                    if (res.data !== null &&
                        res.data.username === formName.username &&
                        res.data.password === formName.password) {

                        localStorage.setItem("access_token", res.data.access_token);
                        this.$router.push('/index');

                    } else {
                        alert('请输入正确的用户名和密码！！！');
                        formName.username = '';
                        formName.password = '';

                    }
                }).then((error)=> this.error = error);
            }
        }
    }
</script>

